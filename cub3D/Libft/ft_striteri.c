#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	x;

	x = 0;
	if (s != NULL && f != NULL)
	{
		while (s[x])
		{
			f(x, &s[x]);
			x++;
		}
	}
}
